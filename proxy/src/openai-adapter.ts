function normalizePathSlashes(path: string): string {
  return path.replace(/\\/g, "/");
}

function isAbsolutePath(path: string): boolean {
  return path.startsWith("/") || /^[A-Za-z]:\//.test(path);
}

function joinPath(base: string, child: string): string {
  if (!base) return child;
  if (!child) return base;
  if (base.endsWith("/")) return `${base}${child.replace(/^\/+/, "")}`;
  return `${base}/${child.replace(/^\/+/, "")}`;
}

function pathBasename(path: string): string {
  const normalized = normalizePathSlashes(path).replace(/\/+$/g, "");
  if (!normalized) return "";
  const idx = normalized.lastIndexOf("/");
  return idx >= 0 ? normalized.slice(idx + 1) : normalized;
}

function pathDirname(path: string): string | undefined {
  const normalized = normalizePathSlashes(path).replace(/\/+$/g, "");
  if (!normalized) return undefined;
  const idx = normalized.lastIndexOf("/");
  if (idx < 0) return undefined;
  if (idx === 0) return "/";
  return normalized.slice(0, idx);
}

const WORKSPACE_MARKER_FILES = [
  ".git",
  "deno.json",
  "package.json",
  "pyproject.toml",
  "go.mod",
  "Cargo.toml",
  "build.zig",
  "README.md",
];

function pathExists(path: string): boolean {
  try {
    Deno.statSync(path);
    return true;
  } catch {
    return false;
  }
}

function directoryExists(path: string): boolean {
  try {
    return Deno.statSync(path).isDirectory;
  } catch {
    return false;
  }
}

function hasFileExtension(path: string): boolean {
  return /\.[^/\\.]+$/.test(pathBasename(path));
}

function workspaceFolderFromPath(path?: string): string | undefined {
  if (!path) return undefined;
  let candidate = canonicalizePath(normalizePathSlashes(path.trim()));
  if (!candidate) return undefined;
  if (!directoryExists(candidate)) {
    candidate = pathDirname(candidate) ?? candidate;
  }
  if (!directoryExists(candidate)) return undefined;

  const homePrefix = allowedHomePrefix();
  const normalizedHome = homePrefix ? canonicalizePath(homePrefix) : undefined;
  for (let depth = 0; depth < 8; depth += 1) {
    if (!directoryExists(candidate)) break;
    const hasMarker = WORKSPACE_MARKER_FILES.some((entry) =>
      pathExists(joinPath(candidate, entry))
    );
    if (hasMarker) return candidate;
    const parent = pathDirname(candidate);
    if (!parent || parent === candidate) break;
    if (
      normalizedHome &&
      (candidate === normalizedHome ||
        !candidate.startsWith(`${normalizedHome}/`))
    ) break;
    candidate = parent;
  }
  return directoryExists(candidate) ? candidate : undefined;
}

function extensionCompletionMatch(
  baseLower: string,
  nameLower: string,
): boolean {
  if (!nameLower.startsWith(baseLower) || nameLower === baseLower) return false;
  const suffix = nameLower.slice(baseLower.length);
  if (suffix.startsWith(".")) return true;
  if (!baseLower.includes(".")) return false;
  return /^[a-z0-9_-]+$/i.test(suffix);
}

function uniqueExtensionSibling(path: string): string | undefined {
  const dir = pathDirname(path);
  const base = pathBasename(path);
  if (!dir || !base || !directoryExists(dir)) return undefined;
  const baseLower = base.toLowerCase();
  const matches: string[] = [];
  for (const entry of Deno.readDirSync(dir)) {
    if (!entry.isFile) continue;
    const nameLower = entry.name.toLowerCase();
    if (extensionCompletionMatch(baseLower, nameLower)) {
      matches.push(entry.name);
    }
  }
  if (matches.length === 1) return joinPath(dir, matches[0]);
  if (matches.length > 1) return undefined;
  const stemIndex = baseLower.lastIndexOf("-");
  if (stemIndex <= 1) return undefined;
  const stem = baseLower.slice(0, stemIndex);
  const stemMatches: string[] = [];
  for (const entry of Deno.readDirSync(dir)) {
    if (!entry.isFile) continue;
    const nameLower = entry.name.toLowerCase();
    if (nameLower.startsWith(`${stem}-`)) stemMatches.push(entry.name);
  }
  if (stemMatches.length === 1) return joinPath(dir, stemMatches[0]);
  return undefined;
}

function uniqueDirectoryPrefixSibling(path: string): string | undefined {
  const dir = pathDirname(path);
  const base = pathBasename(path);
  if (!dir || !base || !directoryExists(dir)) return undefined;
  if (base.length < 2 || hasFileExtension(base)) return undefined;
  const baseLower = base.toLowerCase();
  const matches: string[] = [];
  for (const entry of Deno.readDirSync(dir)) {
    if (!entry.isDirectory) continue;
    const nameLower = entry.name.toLowerCase();
    if (nameLower.startsWith(baseLower)) matches.push(entry.name);
  }
  if (matches.length === 1) return joinPath(dir, matches[0]);
  return undefined;
}

function uniqueFilePrefixSibling(path: string): string | undefined {
  const dir = pathDirname(path);
  const base = pathBasename(path);
  if (!dir || !base || !directoryExists(dir)) return undefined;
  if (base.length < 2 || hasFileExtension(base)) return undefined;
  const baseLower = base.toLowerCase();
  const matches: string[] = [];
  for (const entry of Deno.readDirSync(dir)) {
    if (!entry.isFile) continue;
    const nameLower = entry.name.toLowerCase();
    if (nameLower.startsWith(baseLower)) matches.push(entry.name);
  }
  if (matches.length === 1) return joinPath(dir, matches[0]);
  return undefined;
}

function canonicalizePath(path: string): string {
  let normalized = normalizePathSlashes(path).replace(/\/+/g, "/");
  if (normalized.length > 1) normalized = normalized.replace(/\/+$/g, "");
  return normalized;
}

function allowedHomePrefix(): string | undefined {
  let rawUser = "";
  try {
    rawUser = Deno.env.get("USER") ?? "";
  } catch {
    return undefined;
  }
  const user = rawUser.trim().replace(/^\/+|\/+$/g, "");
  if (!user) return undefined;
  return `/home/${user}`;
}

function allowedHomeHint(): string {
  const prefix = allowedHomePrefix();
  return prefix ? `${prefix}/...` : "/home/<user>/...";
}

function isPathWithinAllowedHome(path: string): boolean {
  const normalized = canonicalizePath(path);
  if (!isAbsolutePath(normalized)) return false;
  if (normalized === "/" || normalized === "/home") return false;
  const userPrefix = allowedHomePrefix();
  if (userPrefix) {
    const prefix = canonicalizePath(userPrefix);
    return normalized === prefix || normalized.startsWith(`${prefix}/`);
  }
  return /^\/home\/[^/]+(?:\/.*)?$/.test(normalized);
}

function repairViewPath(path: string, fallbackPath?: string): string {
  const cleaned = cleanExtractedPath(path);
  if (!cleaned) return cleaned;
  const fallback = fallbackPath
    ? normalizePathSlashes(fallbackPath.trim())
    : undefined;
  const ordered: string[] = [];
  const seen = new Set<string>();
  const addCandidate = (value?: string) => {
    if (!value) return;
    const normalized = normalizePathSlashes(value.trim());
    if (!normalized || seen.has(normalized)) return;
    seen.add(normalized);
    ordered.push(normalized);
  };

  addCandidate(cleaned);
  if (fallback && cleaned === ".") addCandidate(fallback);
  if (fallback && !isAbsolutePath(cleaned)) {
    addCandidate(joinPath(fallback, cleaned.replace(/^\.\//, "")));
  }

  for (const candidate of ordered) {
    if (pathExists(candidate)) return candidate;
  }

  const baseCandidates = [...ordered];
  for (const candidate of baseCandidates) {
    if (hasFileExtension(candidate)) continue;
    for (const ext of VIEW_PATH_EXTENSION_CANDIDATES) {
      addCandidate(`${candidate}${ext}`);
    }
  }

  for (const candidate of ordered) {
    if (pathExists(candidate)) return candidate;
  }
  for (const candidate of ordered) {
    const repaired = uniqueExtensionSibling(candidate);
    if (repaired) return repaired;
  }
  for (const candidate of ordered) {
    const repairedFile = uniqueFilePrefixSibling(candidate);
    if (repairedFile) return repairedFile;
  }
  for (const candidate of ordered) {
    const repairedDir = uniqueDirectoryPrefixSibling(candidate);
    if (repairedDir) return repairedDir;
  }
  for (const candidate of ordered) {
    const dir = pathDirname(candidate);
    const base = pathBasename(candidate);
    if (!dir || !directoryExists(dir)) continue;
    if (base.length <= 1) return dir;
    if (hasFileExtension(base)) return dir;
  }
  return cleaned;
}

function invalidToolReason(
  toolName: string,
  argumentsJson: string,
): string | undefined {
  try {
    const args = JSON.parse(argumentsJson || "{}") as JsonObject;
    if ((toolName === "view") && typeof args.path !== "string") {
      return `Tool ${toolName} requires a concrete path. Retry with valid JSON like {"path":"README.md","type":"file"}.`;
    }
    if (toolName === "view" && Array.isArray(args.view_range)) {
      const range = args.view_range;
      const start = range[0];
      const end = range[1];
      const validRange = range.length === 2 &&
        typeof start === "number" && Number.isFinite(start) &&
        typeof end === "number" && Number.isFinite(end) &&
        start >= 1 && end >= start;
      if (!validRange) {
        return `Tool ${toolName} view_range must be omitted or be [start_line,end_line]. Do not send an empty view_range array.`;
      }
    }
    if (
      (toolName === "view") && typeof args.path === "string" &&
      !isPathWithinAllowedHome(args.path)
    ) {
      return `Tool ${toolName} path is outside the allowed scope. Use an absolute path under ${allowedHomeHint()}.`;
    }
    if (
      (toolName === "view") && typeof args.path === "string" &&
      !pathExists(args.path)
    ) {
      return `Tool ${toolName} path does not exist: ${args.path}. Use an existing path from a directory listing result.`;
    }
    if (toolName === "view-range-untruncated") {
      const missing = ["reference_id", "start_line", "end_line"].filter((key) =>
        args[key] === undefined || args[key] === null || args[key] === ""
      );
      if (missing.length > 0) {
        return `Tool ${toolName} requires ${missing.join(", ")}.`;
      }
      if (typeof args.path === "string" && args.path) {
        return `Tool ${toolName} does not accept path. Use {"reference_id":"<id>","start_line":1,"end_line":80}, or use view for path-based reads.`;
      }
      if (
        typeof args.reference_id === "string" &&
        looksLikeToolUseId(args.reference_id)
      ) {
        return `Tool ${toolName} reference_id must be the truncation footer Reference ID, not a tool_use_id like ${args.reference_id}. If you only know the file path, use view instead.`;
      }
    }
    if (toolName === "launch-process") {
      const missing = ["command", "wait", "max_wait_seconds", "cwd"].filter((
        key,
      ) => args[key] === undefined || args[key] === null || args[key] === "");
      if (missing.length > 0) {
        return `Tool ${toolName} requires ${
          missing.join(", ")
        }. Retry with valid JSON like {"command":"pwd && ls -la","wait":true,"max_wait_seconds":60,"cwd":"/known/directory"}.`;
      }
      if (typeof args.cwd === "string" && !isPathWithinAllowedHome(args.cwd)) {
        return `Tool ${toolName} cwd is outside the allowed scope. Use an absolute cwd under ${allowedHomeHint()}.`;
      }
    }
    if (toolName === "read-process") {
      const missing = ["terminal_id", "wait", "max_wait_seconds"].filter((
        key,
      ) => args[key] === undefined || args[key] === null || args[key] === "");
      if (missing.length > 0) {
        return `Tool ${toolName} requires ${
          missing.join(", ")
        }. Retry only after launch-process returns a terminal_id.`;
      }
    }
    if (
      (toolName === "kill-process" || toolName === "write-process") &&
      (args.terminal_id === undefined || args.terminal_id === null ||
        args.terminal_id === "")
    ) {
      return `Tool ${toolName} requires terminal_id. Retry only after launch-process returns a terminal_id.`;
    }
    if (toolName === "write-process" && typeof args.input_text !== "string") {
      return `Tool ${toolName} requires input_text.`;
    }
    if (toolName === "web-fetch" && typeof args.url !== "string") {
      return `Tool ${toolName} requires url.`;
    }
    if (toolName === "search-untruncated") {
      const missing = ["reference_id", "search_term"].filter((key) =>
        typeof args[key] !== "string" || args[key] === ""
      );
      if (missing.length > 0) {
        return `Tool ${toolName} requires ${missing.join(", ")}.`;
      }
      if (
        typeof args.reference_id === "string" &&
        looksLikeToolUseId(args.reference_id)
      ) {
        return `Tool ${toolName} reference_id must be the truncation footer Reference ID, not a tool_use_id like ${args.reference_id}.`;
      }
    }
    if (
      toolName === "codebase-retrieval" &&
      typeof args.information_request !== "string"
    ) return `Tool ${toolName} requires information_request.`;
    if (
      toolName === "codebase-retrieval" &&
      typeof args.workspace_folder !== "string"
    ) {
      return `Tool ${toolName} requires workspace_folder to avoid ambiguous workspace resolution.`;
    }
    if (toolName === "str-replace-editor") {
      if (typeof args.path !== "string") {
        return `Tool ${toolName} requires path.`;
      }
      if (!isPathWithinAllowedHome(args.path)) {
        return `Tool ${toolName} path is outside the allowed scope. Use an absolute path under ${allowedHomeHint()}.`;
      }
      if (!pathExists(args.path)) {
        return `Tool ${toolName} path does not exist: ${args.path}.`;
      }
      if (args.command === "str_replace") {
        if (
          !Array.isArray(args.str_replace_entries) ||
          args.str_replace_entries.length === 0
        ) {
          return `Tool ${toolName} has no effective str_replace_entries. Re-read the file and build fresh old_str/new_str pairs where new_str is different from old_str before retrying.`;
        }
      }
      if (args.command === "insert") {
        if (
          !Array.isArray(args.insert_line_entries) ||
          args.insert_line_entries.length === 0
        ) {
          return `Tool ${toolName} has no insert_line_entries. Re-read the file and build a non-empty insert edit before retrying.`;
        }
      }
    }
    if (toolName === "save-file") {
      if (typeof args.path !== "string") {
        return `Tool ${toolName} requires path.`;
      }
      if (!isPathWithinAllowedHome(args.path)) {
        return `Tool ${toolName} path is outside the allowed scope. Use an absolute path under ${allowedHomeHint()}.`;
      }
      const content = args.file_content ?? args.content ?? args.file_contents ??
        args.contents ?? args.text ?? args.new_content ?? args.new_contents ??
        args.data ?? args.body;
      if (typeof content !== "string") {
        return `Tool ${toolName} requires file_content.`;
      }
    }
    if (
      toolName === "reorganize_tasklist" && typeof args.markdown !== "string"
    ) return `Tool ${toolName} requires markdown.`;
    if (
      (toolName === "update_tasks" || toolName === "add_tasks") &&
      !Array.isArray(args.tasks)
    ) return `Tool ${toolName} requires tasks array.`;
  } catch {
    return `Tool ${toolName} arguments are not valid JSON. Retry with a valid JSON object.`;
  }
  return undefined;
}

function toolCallsToNodes(
  toolCalls: JsonValue,
  startingId = 1,
  fallbackPath?: string,
  launchCommandFallback?: string,
): JsonObject[] {
  if (!Array.isArray(toolCalls)) return [];
  const nodes: JsonObject[] = [];
  let id = startingId;
  for (const call of toolCalls) {
    const parsed = parseToolCall(call, fallbackPath, launchCommandFallback);
    if (!parsed) continue;
    const invalidReason = invalidToolReason(parsed.name, parsed.argumentsJson);
    if (invalidReason) continue;
    nodes.push({
      id,
      type: 5,
      tool_use: {
        tool_name: parsed.name,
        tool_use_id: parsed.id,
        input_json: parsed.argumentsJson,
      },
    });
    id += 1;
  }
  return nodes;
}

function textResponseNode(content: string, id = 1): JsonObject | undefined {
  const trimmed = content.trim();
  if (!trimmed) return undefined;
  return {
    id,
    type: 0,
    content,
  };
}

function recentToolCallCounts(
  ctx: RequestContext,
  fallbackPath?: string,
): Map<string, number> {
  const counts = new Map<string, number>();
  const body = objectBody(ctx);
  for (const item of asArray(body.chat_history).slice(-8)) {
    if (!item || typeof item !== "object" || Array.isArray(item)) continue;
    const record = item as JsonObject;
    for (const node of asArray(record.response_nodes)) {
      const call = nodeToolUse(node);
      if (!call) continue;
      const parsed = parseToolCall(call, fallbackPath);
      if (parsed) {
        const key = toolCallKey(parsed.name, parsed.argumentsJson);
        counts.set(key, (counts.get(key) ?? 0) + 1);
      }
    }
  }
  return counts;
}

function toolCallKey(name: string, argumentsJson: string): string {
  try {
    return `${name}:${JSON.stringify(JSON.parse(argumentsJson))}`;
  } catch {
    return `${name}:${argumentsJson}`;
  }
}

function looksLikeToolUseId(value: string): boolean {
  return /^call_function_[a-z0-9_]+$/i.test(value.trim());
}

function filterRepeatedToolCalls(
  toolCalls: JsonObject[],
  recentCounts: Map<string, number>,
  fallbackPath?: string,
  launchCommandFallback?: string,
): { valid: JsonObject[]; repeated: JsonObject[] } {
  const valid: JsonObject[] = [];
  const repeated: JsonObject[] = [];
  for (const call of toolCalls) {
    const parsed = parseToolCall(call, fallbackPath, launchCommandFallback);
    if (!parsed) continue;
    const key = toolCallKey(parsed.name, parsed.argumentsJson);
    const seenCount = recentCounts.get(key) ?? 0;
    // Allow one repeat in case the model is reconciling tool output; start
    // filtering only after the same call has appeared multiple times.
    if (seenCount >= 2) repeated.push(call);
    else valid.push(call);
  }
  return { valid, repeated };
}

function invalidToolCallSummaries(
  toolCalls: JsonValue,
  fallbackPath?: string,
  launchCommandFallback?: string,
): JsonObject[] {
  if (!Array.isArray(toolCalls)) return [];
  const output: JsonObject[] = [];
  for (const call of toolCalls) {
    const parsed = parseToolCall(call, fallbackPath, launchCommandFallback);
    if (!parsed) continue;
    const reason = invalidToolReason(parsed.name, parsed.argumentsJson);
    if (reason) {
      output.push({
        id: parsed.id,
        name: parsed.name,
        arguments: parsed.argumentsJson,
        reason,
      });
    }
  }
  return output;
}

function invalidToolCallHint(
  invalidToolCalls: JsonObject[],
): string | undefined {
  if (invalidToolCalls.length === 0) return undefined;
  const first = invalidToolCalls[0];
  const name = typeof first.name === "string" ? first.name : "unknown";
  const reason = typeof first.reason === "string"
    ? first.reason
    : "The tool-call arguments were invalid.";
  if (
    name === "str-replace-editor" &&
    reason.includes("no unapplied str_replace_entries")
  ) {
    return undefined;
  }
  return `Tool call rejected (${name}): ${reason}`;
}

function recoveryToolNodesForInvalidToolCalls(
  invalidToolCalls: JsonObject[],
  startingId = 1,
): JsonObject[] {
  const nodes: JsonObject[] = [];
  const seenPaths = new Set<string>();
  let id = startingId;
  for (const call of invalidToolCalls) {
    if (call.name !== "str-replace-editor") continue;
    const reason = typeof call.reason === "string" ? call.reason : "";
    if (!reason.includes("no effective str_replace_entries")) continue;
    const argumentsJson = typeof call.arguments === "string" ? call.arguments : "{}";
    let args: JsonObject;
    try {
      const parsed = JSON.parse(argumentsJson);
      if (!parsed || typeof parsed !== "object" || Array.isArray(parsed)) continue;
      args = parsed as JsonObject;
    } catch {
      continue;
    }
    const path = typeof args.path === "string" ? args.path : "";
    if (!path || seenPaths.has(path)) continue;
    if (!isPathWithinAllowedHome(path) || !isFilePath(path)) continue;
    seenPaths.add(path);
    const sourceId = typeof call.id === "string" && call.id
      ? call.id
      : `invalid_${id}`;
    nodes.push({
      id,
      type: 5,
      tool_use: {
        tool_name: "view",
        tool_use_id: `${sourceId}_recovery_view`,
        input_json: JSON.stringify({ path, type: "file" }),
      },
    });
    id += 1;
  }
  return nodes;
}

function hasMeaningfulVisibleText(content: string): boolean {
  const trimmed = content.trim();
  if (!trimmed) return false;
  if (trimmed.length >= 16) return true;
  const semanticChars =
    (trimmed.match(/[A-Za-z0-9\u4E00-\u9FFF]/g) ?? []).length;
  return semanticChars >= 4;
}

function mergeStreamToolCalls(toolCalls: JsonObject[]): JsonObject[] {
  const byKey = new Map<string, JsonObject>();
  const order: string[] = [];
  let anonymousCounter = 0;
  let anonymousFallbackCursor = 0;

  const completeJsonObject = (value: string | undefined): boolean => {
    if (!value || !value.trim()) return false;
    try {
      JSON.parse(value);
      return true;
    } catch {
      return false;
    }
  };
  const mergedArgumentsByKey = (key: string): string | undefined => {
    const existing = byKey.get(key);
    const fn = existing?.function && typeof existing.function === "object" &&
        !Array.isArray(existing.function)
      ? existing.function as JsonObject
      : undefined;
    return fn && typeof fn.arguments === "string" ? fn.arguments : undefined;
  };
  const unresolvedKeys = (): string[] => {
    return order.filter((key) => !completeJsonObject(mergedArgumentsByKey(key)));
  };
  const unresolvedNameForKey = (key: string): string => {
    const existing = byKey.get(key);
    const fn = existing?.function && typeof existing.function === "object" &&
        !Array.isArray(existing.function)
      ? existing.function as JsonObject
      : undefined;
    return fn && typeof fn.name === "string" ? fn.name : "";
  };

  for (const call of toolCalls) {
    const index = typeof call.index === "number" ? call.index : undefined;
    const id = typeof call.id === "string" && call.id ? call.id : undefined;
    const sourceFunction = call.function && typeof call.function === "object" &&
        !Array.isArray(call.function)
      ? call.function as JsonObject
      : undefined;
    const sourceName =
      sourceFunction && typeof sourceFunction.name === "string" &&
        sourceFunction.name
        ? sourceFunction.name
        : undefined;
    const sourceArguments =
      sourceFunction && typeof sourceFunction.arguments === "string"
        ? sourceFunction.arguments
        : undefined;

    let key: string | undefined;
    if (id && byKey.has(id)) key = id;
    else if (typeof index === "number") key = `index:${index}`;
    else if (id) key = id;
    else {
      // If upstream omits both index and id (seen in some OpenAI-compatible
      // providers), only stitch into an existing call when there is exactly one
      // unresolved call. When there are multiple unresolved calls, prefer an
      // exact/compatible name match. As a last resort, distribute fragments
      // round-robin across unresolved calls to avoid collapsing everything into
      // one call under parallel tool emission.
      const unresolved = unresolvedKeys();
      if (unresolved.length === 1) key = unresolved[0];
      else if (unresolved.length > 1) {
        if (sourceName) {
          const candidates = unresolved.filter((candidateKey) => {
            const candidateName = unresolvedNameForKey(candidateKey);
            return !candidateName ||
              sourceName === candidateName ||
              sourceName.startsWith(candidateName) ||
              candidateName.startsWith(sourceName);
          });
          if (candidates.length === 1) key = candidates[0];
        }
        if (!key) {
          key = unresolved[anonymousFallbackCursor % unresolved.length];
          anonymousFallbackCursor += 1;
        }
      }
    }
    if (!key) key = `anon:${anonymousCounter++}`;
    let merged = byKey.get(key);
    if (!merged) {
      merged = {
        id: id ?? `tool_${crypto.randomUUID()}`,
        type: "function",
        function: {},
      };
      byKey.set(key, merged);
      order.push(key);
    }

    if (id) merged.id = id;
    if (typeof call.type === "string") merged.type = call.type;

    if (!sourceFunction) continue;

    const mergedFunction =
      (merged.function && typeof merged.function === "object" &&
          !Array.isArray(merged.function))
        ? merged.function as JsonObject
        : {};
    const mergedName = typeof mergedFunction.name === "string"
      ? mergedFunction.name
      : "";
    if (sourceName) {
      if (!mergedName) mergedFunction.name = sourceName;
      else if (sourceName === mergedName || mergedName.startsWith(sourceName)) {
        mergedFunction.name = mergedName;
      } else if (sourceName.startsWith(mergedName)) {
        mergedFunction.name = sourceName;
      } else mergedFunction.name = `${mergedName}${sourceName}`;
    }
    if (typeof sourceArguments === "string") {
      const mergedArgs = typeof mergedFunction.arguments === "string"
        ? mergedFunction.arguments
        : "";
      if (!mergedArgs) mergedFunction.arguments = sourceArguments;
      else if (
        sourceArguments === mergedArgs || mergedArgs.startsWith(sourceArguments)
      ) mergedFunction.arguments = mergedArgs;
      else if (sourceArguments.startsWith(mergedArgs)) {
        mergedFunction.arguments = sourceArguments;
      } else mergedFunction.arguments = `${mergedArgs}${sourceArguments}`;
    }
    merged.function = mergedFunction;
  }

  const mergedCalls = order.map((key) => byKey.get(key)).filter(
    (call): call is JsonObject => {
      const fn = call?.function;
      if (!fn || typeof fn !== "object" || Array.isArray(fn)) return false;
      const record = fn as JsonObject;
      return typeof record.name === "string" && record.name.length > 0;
    },
  );
  return mergedCalls;
}

function renderToolCalls(toolCalls: JsonValue): string {
  if (!Array.isArray(toolCalls) || toolCalls.length === 0) return "";
  return toolCalls.map((call) => {
    const parsed = parseToolCall(call);
    if (!parsed) return "";
    return `<tool_call>\n<${parsed.name}>\n${parsed.argumentsJson}\n</${parsed.name}>\n</tool_call>`;
  }).filter(Boolean).join("\n");
}

function buildOpenAIRequest(
  config: ProxyConfig,
  ctx: RequestContext,
  stream: boolean,
  continuationNudge?: string,
  forceToolChoiceRequired = false,
): OpenAIChatRequest {
  const body = objectBody(ctx);
  const messages = buildMessages(config, ctx);
  if (continuationNudge) {
    messages.push({ role: "user", content: continuationNudge });
  }
  const request: OpenAIChatRequest = {
    model: config.openaiModel,
    messages,
    stream,
  };
  if (typeof body.temperature === "number") {
    request.temperature = body.temperature;
  }
  if (typeof body.max_tokens === "number") request.max_tokens = body.max_tokens;
  if (stream) request.stream_options = { include_usage: true };
  const tools = buildOpenAITools(ctx);
  if (tools.length > 0) {
    request.tools = tools;
    request.tool_choice = forceToolChoiceRequired ? "required" : "auto";
  }
  return request;
}

function numberField(value: JsonValue | undefined): number | undefined {
  return typeof value === "number" && Number.isFinite(value) ? value : undefined;
}

function estimatePromptTokens(request: OpenAIChatRequest): number {
  const chars = request.messages.reduce((sum, message) => {
    const toolCalls = message.tool_calls ? JSON.stringify(message.tool_calls).length : 0;
    return sum + message.content.length + toolCalls;
  }, 0) + JSON.stringify(request.tools ?? []).length;
  return Math.ceil(chars / 4);
}

function estimateTextTokens(value: string): number {
  return Math.ceil(value.length / 4);
}

function estimateJsonTokens(value: JsonValue | undefined): number {
  if (value === undefined) return 0;
  return estimateTextTokens(JSON.stringify(value));
}

function estimateMessageTokens(message: OpenAIMessage): number {
  return estimateTextTokens(message.content) + estimateJsonTokens(message.tool_calls);
}

function estimateToolResultTokens(messages: OpenAIMessage[]): number {
  return messages.reduce((sum, message) => {
    return message.role === "tool" ? sum + estimateMessageTokens(message) : sum;
  }, 0);
}

function estimateSystemPromptTokens(messages: OpenAIMessage[]): number {
  const first = messages[0];
  return first?.role === "system" ? estimateMessageTokens(first) : 0;
}

function estimateCurrentMessageTokens(messages: OpenAIMessage[]): number {
  for (let index = messages.length - 1; index >= 0; index -= 1) {
    const message = messages[index];
    if (message.role === "user") return estimateMessageTokens(message);
  }
  return 0;
}

function estimateAssistantResponseTokens(usage: JsonObject): number {
  return numberField(usage.completion_tokens) ?? 0;
}

function estimateToolDefinitionTokens(request: OpenAIChatRequest): number {
  return estimateJsonTokens(request.tools);
}

function tokenUsageNode(
  config: ProxyConfig,
  request: OpenAIChatRequest,
  usage: JsonValue,
): JsonObject {
  return {
    id: 10_000,
    type: 10,
    token_usage: augmentTokenUsage(config, request, usage),
  };
}

function augmentTokenUsage(
  config: ProxyConfig,
  request: OpenAIChatRequest,
  usage: JsonValue,
): JsonObject {
  const record = usage && typeof usage === "object" && !Array.isArray(usage)
    ? usage as JsonObject
    : {};
  const promptTokens = numberField(record.prompt_tokens) ?? estimatePromptTokens(request);
  const completionTokens = numberField(record.completion_tokens) ?? 0;
  const systemPromptTokens = estimateSystemPromptTokens(request.messages);
  const currentMessageTokens = estimateCurrentMessageTokens(request.messages);
  const toolDefinitionsTokens = estimateToolDefinitionTokens(request);
  const toolResultTokens = estimateToolResultTokens(request.messages);
  const assistantResponseTokens = estimateAssistantResponseTokens(record);
  const knownPromptTokens = systemPromptTokens + currentMessageTokens +
    toolDefinitionsTokens + toolResultTokens;
  const chatHistoryTokens = Math.max(0, promptTokens - knownPromptTokens);
  return {
    input_tokens: promptTokens,
    output_tokens: completionTokens,
    cache_read_input_tokens: numberField(record.cached_tokens) ?? 0,
    cache_creation_input_tokens: 0,
    system_prompt_tokens: systemPromptTokens,
    chat_history_tokens: chatHistoryTokens,
    current_message_tokens: currentMessageTokens,
    tool_definitions_tokens: toolDefinitionsTokens,
    tool_result_tokens: toolResultTokens,
    assistant_response_tokens: assistantResponseTokens,
    max_context_tokens: config.augmentModelContextTokens,
    max_output_tokens: config.augmentModelMaxOutputTokens,
  };
}

function openAIUsage(config: ProxyConfig, request: OpenAIChatRequest, usage: JsonValue): JsonObject {
  const tokenUsage = augmentTokenUsage(config, request, usage);
  const promptTokens = numberField(tokenUsage.input_tokens) ?? 0;
  const completionTokens = numberField(tokenUsage.output_tokens) ?? 0;
  return {
    prompt_tokens: promptTokens,
    completion_tokens: completionTokens,
    total_tokens: promptTokens + completionTokens,
  };
}

function openAIHeaders(config: ProxyConfig, stream: boolean): Headers {
  const headers = new Headers();
  headers.set("authorization", `Bearer ${config.openaiApiKey}`);
  headers.set("content-type", "application/json");
  headers.set("accept", stream ? "text/event-stream" : "application/json");
  if (config.openaiUserAgent) headers.set("user-agent", config.openaiUserAgent);
  return headers;
}

function openAIUrl(config: ProxyConfig): string {
  return `${config.openaiBaseUrl}/chat/completions`;
}

function delay(ms: number): Promise<void> {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

const STREAM_RATE_LIMIT_RETRY_MS = 60_000;

function summarizeUpstreamFailure(
  status: number,
  raw: string,
  hasBody: boolean,
): string {
  const trimmed = raw.trim();
  const bodyText = trimmed ? trimmed.slice(0, 1000) : "<empty body>";
  return `status ${status}, has_body=${hasBody}, body=${bodyText}`;
}

function shouldRetryUpstreamFailure(
  status: number,
  raw: string,
  hasBody: boolean,
  attempt: number,
): boolean {
  if (attempt >= 2) return false;
  if (!raw.trim()) return true;
  if (!hasBody) return true;
  return status === 408 || status === 409 || status === 425 || status === 429 ||
    status === 500 || status === 502 || status === 503 || status === 504;
}

function shouldFallbackToolChoiceAuto(
  status: number,
  raw: string,
): boolean {
  if (status !== 400) return false;
  const lower = raw.toLowerCase();
  return lower.includes("tool_choice") &&
    (lower.includes("required") || lower.includes("unsupported") ||
      lower.includes("invalid"));
}

function augmentStatus(status: number): number {
  if (status === 401 || status === 403) return 502;
  return status;
}

export async function forwardAugmentJson(
  config: ProxyConfig,
  ctx: RequestContext,
): Promise<Response> {
  const request = buildOpenAIRequest(config, ctx, false);
  const fallbackPath = workspaceFallbackPath(ctx);
  logInfo(config, "openai:json:start", {
    requestId: ctx.requestId,
    model: config.openaiModel,
    url: openAIUrl(config),
  });
  logInfo(config, "openai:json:payload", {
    requestId: ctx.requestId,
    messages: request.messages.length,
    tools: request.tools?.length ?? 0,
    bytes: JSON.stringify(request).length,
  });
  const requestBody = JSON.stringify(request);
  let upstream: Response | undefined;
  let raw = "";
  let attempts = 0;
  while (attempts < 2) {
    attempts += 1;
    try {
      upstream = await fetch(openAIUrl(config), {
        method: "POST",
        headers: openAIHeaders(config, false),
        body: requestBody,
      });
    } catch (error) {
      const message = error instanceof Error ? error.message : String(error);
      const retry = attempts < 2;
      logWarn(config, "openai:json:fetch-error", {
        requestId: ctx.requestId,
        attempt: attempts,
        willRetry: retry,
        error: message,
      });
      if (retry) {
        await delay(400);
        continue;
      }
      return augmentError(
        `OpenAI upstream request failed after ${attempts} attempts: ${message}`,
        502,
        "upstream_error",
      );
    }

    raw = await upstream.text().catch(() => "");
    logInfo(config, "openai:json:end", {
      requestId: ctx.requestId,
      status: upstream.status,
      attempt: attempts,
    });
    if (upstream.ok && raw.trim()) break;

    const retry = shouldRetryUpstreamFailure(
      upstream.status,
      raw,
      false,
      attempts,
    );
    logWarn(config, "openai:json:bad-status", {
      requestId: ctx.requestId,
      attempt: attempts,
      willRetry: retry,
      status: upstream.status,
      hasBody: false,
      bodyLen: raw.length,
      contentType: upstream.headers.get("content-type") ?? "",
      upstreamRequestId: upstream.headers.get("x-request-id") ??
        upstream.headers.get("request-id") ?? "",
      body: raw.slice(0, 300),
    });
    if (!retry) break;
    await delay(400);
  }

  if (!upstream) {
    return augmentError(
      "OpenAI upstream request failed before any response was received",
      502,
      "upstream_error",
    );
  }

  if (!upstream.ok || !raw.trim()) {
    return augmentError(
      `OpenAI upstream failed: ${
        summarizeUpstreamFailure(upstream.status, raw, false)
      } (attempts ${attempts})`,
      augmentStatus(upstream.status),
      "upstream_error",
    );
  }

  let data: JsonObject;
  try {
    data = JSON.parse(raw) as JsonObject;
  } catch {
    return augmentError(
      "OpenAI upstream returned invalid JSON",
      502,
      "bad_upstream_json",
    );
  }

  const choices = Array.isArray(data.choices) ? data.choices : [];
  const first = choices[0];
  let content = "";
  let nodes: JsonObject[] = [];
  let reasoningFallback = "";
  if (first && typeof first === "object" && !Array.isArray(first)) {
    const message = (first as JsonObject).message;
    if (message && typeof message === "object" && !Array.isArray(message)) {
      const messageRecord = message as JsonObject;
      content = text(messageRecord.content);
      const reasoning = collectReasoningFields(messageRecord);
      reasoningFallback = reasoning.join("\n\n").trim();
      if (reasoning.length > 0) nodes = [...thinkingNodes(reasoning), ...nodes];
      nodes = [
        ...nodes,
        ...toolCallsToNodes(messageRecord.tool_calls, 1, fallbackPath),
      ];
      if (nodes.length === 0) {
        const renderedToolCalls = renderToolCalls(
          (message as JsonObject).tool_calls,
        );
        if (renderedToolCalls) {
          content = content
            ? `${content}\n${renderedToolCalls}`
            : renderedToolCalls;
        }
      }
    }
    content ||= text((first as JsonObject).text);
  }

  const requestId = ctx.requestId;
  const split = splitThinkingTags(content);
  content = split.visible;
  if (
    !content && reasoningFallback &&
    nodes.every((node) => !(node as JsonObject).tool_use)
  ) {
    content = reasoningFallback;
    nodes = [];
  }
  const responseNode = textResponseNode(content, 1);
  let nextNodeId = responseNode ? 2 : 1;
  const thinking = thinkingNodes(split.thinking).map((node) => ({
    ...node,
    id: nextNodeId++,
  }));
  const toolNodes = nodes.map((node) => ({ ...node, id: nextNodeId++ }));
  nodes = [
    ...(responseNode ? [responseNode] : []),
    ...thinking,
    ...toolNodes,
    tokenUsageNode(config, request, data.usage),
  ];
  const tokenUsage = augmentTokenUsage(config, request, data.usage);
  const usage = openAIUsage(config, request, data.usage);
  return jsonResponse({
    text: content,
    response_text: content,
    completion: content,
    request_id: requestId,
    requestId,
    stop_reason: "stop",
    token_usage: tokenUsage,
    total_tokens: usage.total_tokens,
    usage,
    nodes,
  });
}

function sseEncode(value: JsonObject): string {
  return `${JSON.stringify(value)}\n`;
}

function chunkText(value: string, size: number): string[] {
  const chunks: string[] = [];
  for (let index = 0; index < value.length; index += size) {
    chunks.push(value.slice(index, index + size));
  }
  return chunks.length > 0 ? chunks : [""];
}

function compactStreamResponse(
  config: ProxyConfig,
  ctx: RequestContext,
  compactSummary: string,
): Response {
  const compactRequest = buildOpenAIRequest(config, ctx, true);
  const tokenUsage = augmentTokenUsage(config, compactRequest, null);
  const usage = openAIUsage(config, compactRequest, null);
  const requestId = ctx.requestId;
  const stream = new ReadableStream<Uint8Array>({
    start(controller) {
      const encoder = new TextEncoder();
      let closed = false;
      const enqueue = (value: JsonObject) => {
        if (closed) return;
        controller.enqueue(encoder.encode(sseEncode(value)));
      };
      const heartbeat = setInterval(() => {
        enqueue({ text: "", heartbeat: true, request_id: requestId });
      }, 5_000);
      try {
        enqueue({ text: "", heartbeat: true, request_id: requestId });
        enqueue({
          text: "",
          token_usage: tokenUsage,
          total_tokens: usage.total_tokens,
          usage,
          nodes: [tokenUsageNode(config, compactRequest, null)],
          request_id: requestId,
        });
        for (const chunk of chunkText(compactSummary, 1800)) {
          enqueue({ text: chunk, delta: chunk, request_id: requestId });
        }
        enqueue({
          text: "",
          response_text: compactSummary,
          completion: compactSummary,
          token_usage: tokenUsage,
          total_tokens: usage.total_tokens,
          usage,
          nodes: [{ id: 1, type: 0, content: compactSummary }],
          done: true,
          stop_reason: "stop",
          request_id: requestId,
        });
      } finally {
        closed = true;
        clearInterval(heartbeat);
        controller.close();
      }
    },
  });
  return new Response(stream, {
    status: 200,
    headers: {
      "content-type": "application/json; charset=utf-8",
      "cache-control": "no-cache",
      "x-request-id": requestId,
    },
  });
}

function renderDeltaToolCalls(toolCalls: JsonValue): string {
  return renderToolCalls(toolCalls);
}

function extractDelta(line: string): string | undefined {
  if (!line.startsWith("data:")) return undefined;
  const payload = line.slice(5).trim();
  if (!payload || payload === "[DONE]") {
    return payload === "[DONE]" ? "" : undefined;
  }
  try {
    const data = JSON.parse(payload) as JsonObject;
    const choices = Array.isArray(data.choices) ? data.choices : [];
    const first = choices[0];
    if (!first || typeof first !== "object" || Array.isArray(first)) {
      return undefined;
    }
    const delta = (first as JsonObject).delta;
    if (delta && typeof delta === "object" && !Array.isArray(delta)) {
      const content = text((delta as JsonObject).content);
      const toolText = renderDeltaToolCalls((delta as JsonObject).tool_calls);
      return content || toolText || undefined;
    }
    return text((first as JsonObject).text) || undefined;
  } catch {
    return undefined;
  }
}

class ThinkingStreamFilter {
  private mode: "visible" | "thinking" = "visible";
  private pending = "";
  private thinkingCurrent = "";
  private inlineCode = false;
  private fencedCode = false;
  readonly thinking: string[] = [];

  private consumeVisibleChar(): string {
    const char = this.pending[0];
    if (this.pending.startsWith("```")) {
      this.fencedCode = !this.fencedCode;
      this.pending = this.pending.slice(3);
      return "```";
    }
    if (!this.fencedCode && char === "`") this.inlineCode = !this.inlineCode;
    this.pending = this.pending.slice(1);
    return char;
  }

  private inCode(): boolean {
    return this.inlineCode || this.fencedCode;
  }

  push(input: string): string {
    this.pending += input;
    let visible = "";
    while (this.pending.length > 0) {
      if (this.mode === "visible") {
        if (this.inCode()) {
          visible += this.consumeVisibleChar();
          continue;
        }
        const open = this.pending.search(/<\/?(?:think|thinking|reason)\b/i);
        if (open < 0) {
          const keep = Math.min(this.pending.length, 16);
          const flushLen = Math.max(0, this.pending.length - keep);
          for (let index = 0; index < flushLen;) {
            if (this.pending.startsWith("```")) {
              visible += this.consumeVisibleChar();
              index += 3;
            } else {
              visible += this.consumeVisibleChar();
              index += 1;
            }
          }
          break;
        }
        for (let index = 0; index < open;) {
          if (this.pending.startsWith("```")) {
            visible += this.consumeVisibleChar();
            index += 3;
          } else {
            visible += this.consumeVisibleChar();
            index += 1;
          }
        }
        if (this.inCode()) continue;
        const match = this.pending.match(/^<(think|thinking|reason)\b[^>]*>/i);
        if (!match) {
          if (this.pending.length < 32) break;
          visible += this.consumeVisibleChar();
          continue;
        }
        this.pending = this.pending.slice(match[0].length);
        this.mode = "thinking";
        this.thinkingCurrent = "";
      } else {
        const close = this.pending.search(/<\/(?:think|thinking|reason)>/i);
        if (close < 0) {
          this.thinkingCurrent += this.pending;
          this.pending = "";
          break;
        }
        this.thinkingCurrent += this.pending.slice(0, close);
        const closeMatch = this.pending.slice(close).match(
          /^<\/(?:think|thinking|reason)>/i,
        );
        this.pending = this.pending.slice(
          close + (closeMatch?.[0].length ?? 0),
        );
        const thought = this.thinkingCurrent.trim();
        if (thought) this.thinking.push(thought);
        this.thinkingCurrent = "";
        this.mode = "visible";
      }
    }
    return visible;
  }

  flush(): { visible: string; thinking: string[] } {
    let visible = "";
    if (this.mode === "thinking") {
      const thought = (this.thinkingCurrent + this.pending).trim();
      if (thought) this.thinking.push(thought);
    } else {
      visible = this.pending;
    }
    this.pending = "";
    this.thinkingCurrent = "";
    this.mode = "visible";
    return { visible, thinking: this.thinking };
  }
}

function parseOpenAIStreamLine(
  line: string,
): {
  content?: string;
  thinking?: string[];
  toolCalls?: JsonObject[];
  usage?: JsonValue;
  done?: boolean;
  finishReason?: string;
} {
  if (!line.startsWith("data:")) return {};
  const payload = line.slice(5).trim();
  if (!payload) return {};
  if (payload === "[DONE]") return { done: true };
  try {
    const data = JSON.parse(payload) as JsonObject;
    const usage = data.usage;
    const choices = Array.isArray(data.choices) ? data.choices : [];
    const first = choices[0];
    if (!first || typeof first !== "object" || Array.isArray(first)) {
      return usage ? { usage } : {};
    }
    const finishReason = text((first as JsonObject).finish_reason) || undefined;
    const delta = (first as JsonObject).delta;
    if (!delta || typeof delta !== "object" || Array.isArray(delta)) {
      return { finishReason };
    }
    const deltaRecord = delta as JsonObject;
    return {
      content: text(deltaRecord.content) || undefined,
      thinking: collectReasoningFields(deltaRecord),
      toolCalls: Array.isArray(deltaRecord.tool_calls)
        ? deltaRecord.tool_calls as JsonObject[]
        : undefined,
      usage,
      finishReason,
    };
  } catch {
    return {};
  }
}

export async function forwardAugmentStream(
  config: ProxyConfig,
  ctx: RequestContext,
): Promise<Response> {
  const compactSummary = compactSummaryText(ctx);
  if (compactSummary) {
    logInfo(config, "openai:stream:compact-local", {
      requestId: ctx.requestId,
      chars: compactSummary.length,
    });
    return compactStreamResponse(config, ctx, compactSummary);
  }
  const continuationNudge = shouldRetryStalledContinuation(ctx)
    ? [
      "Continuation control: do not stop after a brief status update.",
      "If the task is not fully complete, call the next appropriate tool now.",
      "Only produce a final natural-language answer when there is no concrete tool action left to take.",
    ].join(" ")
    : undefined;
  const forceToolChoiceRequired = Boolean(continuationNudge);
  const request = buildOpenAIRequest(
    config,
    ctx,
    true,
    continuationNudge,
    forceToolChoiceRequired,
  );
  const requestId = ctx.requestId;
  const fallbackPath = workspaceFallbackPath(ctx);
  const streamToolCalls: JsonObject[] = [];

  const stream = new ReadableStream<Uint8Array>({
    start(controller) {
      const encoder = new TextEncoder();
      const decoder = new TextDecoder();
      let buffer = "";
      const thinkingFilter = new ThinkingStreamFilter();
      const thinkingBuffer: string[] = [];
      let visibleText = "";
      let emittedVisibleText = false;
      let sawDone = false;
      let upstreamChunks = 0;
      let upstreamContentChars = 0;
      let streamUsage: JsonValue | undefined;
      let finishReason = "";
      let closed = false;
      const safeEnqueue = (value: JsonObject) => {
        if (closed) return;
        try {
          controller.enqueue(encoder.encode(sseEncode(value)));
        } catch {
          closed = true;
        }
      };
      const enqueueTerminalError = (message: string) => {
        safeEnqueue({
          text: message,
          error: message,
          response_text: message,
          completion: message,
          done: true,
          stop_reason: "error",
          request_id: requestId,
        });
        finish();
      };
      const enqueueTerminalText = (message: string, extra: JsonObject = {}) => {
        safeEnqueue({
          text: message,
          response_text: message,
          completion: message,
          done: true,
          stop_reason: "stop",
          request_id: requestId,
          ...extra,
        });
        finish();
      };
      const finish = () => {
        if (closed) return;
        closed = true;
        clearInterval(heartbeat);
        try {
          controller.close();
        } catch {
          // stream already closed by client
        }
      };
      const heartbeat = setInterval(() => {
        safeEnqueue({ text: "", heartbeat: true, request_id: requestId });
      }, 5_000);

      // Send an immediate chunk so Augment receives response headers before its headers timeout.
      safeEnqueue({ text: "", heartbeat: true, request_id: requestId });
      logInfo(config, "openai:stream:token-usage-initial", {
        requestId,
        estimatedInputTokens: estimatePromptTokens(request),
        maxContextTokens: config.augmentModelContextTokens,
        maxOutputTokens: config.augmentModelMaxOutputTokens,
      });
      const initialTokenUsage = augmentTokenUsage(config, request, null);
      const initialUsage = openAIUsage(config, request, null);
      safeEnqueue({
        text: "",
        token_usage: initialTokenUsage,
        total_tokens: initialUsage.total_tokens,
        usage: initialUsage,
        nodes: [tokenUsageNode(config, request, null)],
        request_id: requestId,
      });

      void (async () => {
        try {
          let upstream: Response | undefined;
          let upstreamAttempts = 0;
          logInfo(config, "openai:stream:start", {
            requestId,
            model: config.openaiModel,
            url: openAIUrl(config),
          });
          logInfo(config, "openai:stream:payload", {
            requestId,
            messages: request.messages.length,
            tools: request.tools?.length ?? 0,
            bytes: JSON.stringify(request).length,
          });
          let requestBody = JSON.stringify(request);
          let usedToolChoiceFallback = false;
          while (true) {
            upstreamAttempts += 1;
            const waitLogger = setInterval(
              () =>
                logInfo(config, "openai:stream:waiting-headers", {
                  requestId,
                  attempt: upstreamAttempts,
                }),
              15_000,
            );
            try {
              upstream = await fetch(openAIUrl(config), {
                method: "POST",
                headers: openAIHeaders(config, true),
                body: requestBody,
              });
            } catch (error) {
              clearInterval(waitLogger);
              const message = error instanceof Error ? error.message : String(error);
              const retry = upstreamAttempts < 2;
              logError(config, "openai:stream:fetch-error", {
                requestId,
                attempt: upstreamAttempts,
                willRetry: retry,
                error: message,
              });
              if (retry) {
                await delay(400);
                continue;
              }
              enqueueTerminalError(
                `OpenAI stream request failed after ${upstreamAttempts} attempts: ${message}`,
              );
              return;
            } finally {
              clearInterval(waitLogger);
            }

            logInfo(config, "openai:stream:headers", {
              requestId,
              status: upstream.status,
              attempt: upstreamAttempts,
            });

            if (upstream.ok && upstream.body) break;

            const raw = await upstream.text().catch(() => "");
            if (
              forceToolChoiceRequired && !usedToolChoiceFallback &&
              shouldFallbackToolChoiceAuto(upstream.status, raw)
            ) {
              const fallbackRequest = {
                ...request,
                tool_choice: "auto" as const,
              };
              requestBody = JSON.stringify(fallbackRequest);
              usedToolChoiceFallback = true;
              logWarn(config, "openai:stream:tool-choice-required-fallback", {
                requestId,
                attempt: upstreamAttempts,
                status: upstream.status,
                body: raw.slice(0, 240),
              });
              await delay(200);
              continue;
            }
            if (upstream.status === 429) {
              logWarn(config, "openai:stream:rate-limited-retry", {
                requestId,
                attempt: upstreamAttempts,
                retryAfterMs: STREAM_RATE_LIMIT_RETRY_MS,
                hasBody: Boolean(upstream.body),
                bodyLen: raw.length,
                contentType: upstream.headers.get("content-type") ?? "",
                upstreamRequestId: upstream.headers.get("x-request-id") ??
                  upstream.headers.get("request-id") ?? "",
                body: raw.slice(0, 300),
              });
              await delay(STREAM_RATE_LIMIT_RETRY_MS);
              if (closed) return;
              continue;
            }
            const retry = shouldRetryUpstreamFailure(
              upstream.status,
              raw,
              Boolean(upstream.body),
              upstreamAttempts,
            );
            logWarn(config, "openai:stream:bad-status", {
              requestId,
              attempt: upstreamAttempts,
              willRetry: retry,
              status: upstream.status,
              hasBody: Boolean(upstream.body),
              bodyLen: raw.length,
              contentType: upstream.headers.get("content-type") ?? "",
              upstreamRequestId: upstream.headers.get("x-request-id") ??
                upstream.headers.get("request-id") ?? "",
              body: raw.slice(0, 300),
            });
            if (retry) {
              await delay(400);
              continue;
            }
            if (!raw.trim()) {
              logWarn(config, "openai:stream:empty-upstream-heartbeat-only", {
                requestId,
                attempt: upstreamAttempts,
                status: upstream.status,
              });
              return;
            }
            enqueueTerminalError(
              `OpenAI stream upstream failed: ${
                summarizeUpstreamFailure(
                  upstream.status,
                  raw,
                  Boolean(upstream.body),
                )
              } (attempts ${upstreamAttempts})`,
            );
            return;
          }

          if (!upstream) {
            logWarn(config, "openai:stream:no-upstream-after-retries", {
              requestId,
              attempts: upstreamAttempts,
            });
            return;
          }

          const reader = upstream.body.getReader();
          while (true) {
            const { value, done } = await reader.read();
            if (done) break;
            if (!value) continue;
            buffer += decoder.decode(value, { stream: true });
            const lines = buffer.split(/\r?\n/);
            buffer = lines.pop() ?? "";
            for (const line of lines) {
              const parsed = parseOpenAIStreamLine(line);
              if (parsed.done) sawDone = true;
              if (parsed.usage !== undefined) streamUsage = parsed.usage;
              if (parsed.finishReason) finishReason = parsed.finishReason;
              if (parsed.toolCalls?.length) {
                streamToolCalls.push(...parsed.toolCalls);
              }
              if (parsed.thinking?.length) {
                thinkingBuffer.push(...parsed.thinking);
              }
              if (parsed.content) {
                upstreamChunks += 1;
                upstreamContentChars += parsed.content.length;
                const visible = thinkingFilter.push(parsed.content);
                if (visible) {
                  emittedVisibleText = true;
                  visibleText += visible;
                  safeEnqueue({
                    text: visible,
                    delta: visible,
                    request_id: requestId,
                  });
                }
              }
            }
          }
          const flushed = thinkingFilter.flush();
          if (flushed.visible) {
            emittedVisibleText = true;
            visibleText += flushed.visible;
            safeEnqueue({
              text: flushed.visible,
              delta: flushed.visible,
              request_id: requestId,
            });
          }
          const launchCommandFallback = inferLaunchCommandFromContext(ctx);
          const mergedToolCalls = mergeStreamToolCalls(streamToolCalls);
          if (streamToolCalls.length > 0) {
            logInfo(config, "openai:stream:tool-calls", {
              requestId,
              fragments: streamToolCalls.length,
              merged: mergedToolCalls.length,
            });
          }
          const invalidToolCalls = invalidToolCallSummaries(
            mergedToolCalls,
            fallbackPath,
            launchCommandFallback,
          );
          if (invalidToolCalls.length > 0) {
            logWarn(config, "openai:stream:invalid-tool-calls-filtered", {
              requestId,
              invalidToolCalls,
            });
          }
          const nonInvalidToolCalls = mergedToolCalls.filter((call) => {
            const parsed = parseToolCall(
              call,
              fallbackPath,
              launchCommandFallback,
            );
            return parsed &&
              !invalidToolReason(parsed.name, parsed.argumentsJson);
          });
          const validToolNodes = toolCallsToNodes(
            nonInvalidToolCalls,
            1,
            fallbackPath,
            launchCommandFallback,
          );
          const recoveryToolNodes = validToolNodes.length === 0
            ? recoveryToolNodesForInvalidToolCalls(invalidToolCalls, 1)
            : [];
          if (recoveryToolNodes.length > 0) {
            logWarn(config, "openai:stream:invalid-tool-recovery-nodes", {
              requestId,
              nodes: recoveryToolNodes.map((node) =>
                (node.tool_use as JsonObject | undefined)?.tool_name ??
                  "unknown"
              ),
              inputs: recoveryToolNodes.map((node) => {
                const input =
                  ((node.tool_use as JsonObject | undefined)?.input_json ??
                    "") as string;
                return { len: input.length, preview: input.slice(0, 120) };
              }),
            });
          }
          const toolNodes = recoveryToolNodes.length > 0
            ? recoveryToolNodes
            : validToolNodes;
          if (validToolNodes.length > 0) {
            logInfo(config, "openai:stream:tool-nodes", {
              requestId,
              nodes: validToolNodes.map((node) =>
                (node.tool_use as JsonObject | undefined)?.tool_name ??
                  "unknown"
              ),
              inputs: validToolNodes.map((node) => {
                const input =
                  ((node.tool_use as JsonObject | undefined)?.input_json ??
                    "") as string;
                return { len: input.length, preview: input.slice(0, 120) };
              }),
              fallbackPathUsed: fallbackPath,
            });
          }
          if (invalidToolCalls.length > 0 && toolNodes.length === 0) {
            const invalidHint = invalidToolCallHint(invalidToolCalls);
            if (invalidHint) {
              const hintText = visibleText.trim()
                ? `\n\n${invalidHint}`
                : invalidHint;
              visibleText += hintText;
              emittedVisibleText = true;
              safeEnqueue({
                text: hintText,
                delta: hintText,
                request_id: requestId,
              });
            }
          }
          const allThinking = [...thinkingBuffer, ...flushed.thinking].filter((
            item,
          ) => item.trim());
          logInfo(config, "openai:stream:end", {
            requestId,
            sawDone,
            finishReason,
            upstreamChunks,
            upstreamContentChars,
            thinkingItems: allThinking.length,
            toolFragments: streamToolCalls.length,
          });
          const responseNode = textResponseNode(visibleText, 1);
          let nextNodeId = responseNode ? 2 : 1;
          const thoughtNodes = hasMeaningfulVisibleText(visibleText)
            ? []
            : thinkingNodes(allThinking).map((node) => ({
              ...node,
              id: nextNodeId++,
            }));
          const persistedToolNodes = toolNodes.map((node) => ({
            ...node,
            id: nextNodeId++,
          }));
          const finalNodes = [
            ...(responseNode ? [responseNode] : []),
            ...thoughtNodes,
            ...persistedToolNodes,
            tokenUsageNode(config, request, streamUsage ?? null),
          ];
          if (
            !hasMeaningfulVisibleText(visibleText) && finalNodes.length === 0
          ) {
            logWarn(config, "openai:stream:empty-keepalive", {
              requestId,
              sawDone,
              finishReason,
              upstreamChunks,
              upstreamContentChars,
            });
            enqueueTerminalText("", {
              heartbeat: true,
              empty_upstream: true,
            });
            return;
          }
          if (finalNodes.length > 0) {
            safeEnqueue({ text: "", nodes: finalNodes, request_id: requestId });
          }
          logInfo(config, "openai:stream:final", {
            requestId,
            visibleChars: visibleText.length,
            nodes: finalNodes.length,
          });
          const finalText = emittedVisibleText ? "" : visibleText;
          const finalTokenUsage = augmentTokenUsage(
            config,
            request,
            streamUsage ?? null,
          );
          const finalUsage = openAIUsage(config, request, streamUsage ?? null);
          safeEnqueue({
            text: finalText,
            response_text: visibleText,
            completion: visibleText,
            token_usage: finalTokenUsage,
            total_tokens: finalUsage.total_tokens,
            usage: finalUsage,
            done: true,
            stop_reason: "stop",
            request_id: requestId,
          });
          finish();
        } catch (error) {
          logError(config, "openai:stream:error", {
            requestId,
            error: String(error),
          });
          enqueueTerminalError(String(error));
        }
      })();
    },
  });

  return new Response(stream, {
    status: 200,
    headers: {
      "content-type": "application/json; charset=utf-8",
      "cache-control": "no-cache",
      "x-request-id": requestId,
    },
  });
}

export async function forwardCompletion(
  config: ProxyConfig,
  ctx: RequestContext,
): Promise<Response> {
  const response = await forwardAugmentJson(config, ctx);
  if (!response.ok) return response;
  const data = await response.json() as JsonObject;
  const content = typeof data.text === "string" ? data.text : "";
  return jsonResponse({
    completion_items: [{ text: content }],
    completion: content,
    text: content,
    request_id: ctx.requestId,
  });
}
