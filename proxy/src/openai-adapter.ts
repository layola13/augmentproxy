function objectBody(ctx: RequestContext): JsonObject {
  return ctx.body && typeof ctx.body === "object" && !Array.isArray(ctx.body)
    ? ctx.body
    : {};
}

function text(value: JsonValue | undefined): string {
  if (typeof value === "string") return value;
  if (typeof value === "number" || typeof value === "boolean") {
    return String(value);
  }
  if (value === null || value === undefined) return "";
  return JSON.stringify(value, null, 2);
}

function collectReasoningFields(value: JsonObject): string[] {
  const output: string[] = [];
  for (const key of ["reasoning_content", "reasoning", "thinking", "reason"]) {
    const field = value[key];
    if (typeof field === "string" && field.trim()) output.push(field.trim());
    else if (field && typeof field === "object" && !Array.isArray(field)) {
      const nested = field as JsonObject;
      const nestedText = text(nested.content ?? nested.summary ?? nested.text);
      if (nestedText.trim()) output.push(nestedText.trim());
    }
  }
  return output;
}

function asArray(value: JsonValue | undefined): JsonValue[] {
  return Array.isArray(value) ? value : [];
}

function addIfPresent(
  parts: string[],
  label: string,
  value: JsonValue | undefined,
): void {
  const rendered = text(value).trim();
  if (rendered) parts.push(`${label}:\n${rendered}`);
}

function truncateMiddle(value: string, maxChars: number): string {
  if (value.length <= maxChars) return value;
  const head = Math.floor(maxChars * 0.6);
  const tail = Math.max(0, maxChars - head);
  return `${value.slice(0, head)}\n\n... [truncated ${
    value.length - maxChars
  } chars] ...\n\n${value.slice(value.length - tail)}`;
}

function nodeText(node: JsonValue): string {
  if (!node || typeof node !== "object" || Array.isArray(node)) return "";
  const record = node as JsonObject;
  if (record.tool_result_node) return "";
  const textNode = record.text_node;
  if (textNode && typeof textNode === "object" && !Array.isArray(textNode)) {
    return text((textNode as JsonObject).content);
  }
  return text(record.content);
}

function compactToolResultContent(content: string): string {
  return truncateMiddle(content, 4_000);
}

function compactSummaryText(ctx: RequestContext): string | undefined {
  const body = objectBody(ctx);
  const current = currentNodeUserText(body.nodes) || text(body.message);
  if (!current.includes("__AUGMENTPROXY_COMPACT__")) return undefined;

  const exchanges = asArray(body.chat_history).slice(-12);
  const lines: string[] = [
    "# Compact Conversation Summary",
    "",
    "This summary is generated by augmentproxy for continuing the current session with reduced context.",
    "",
  ];
  const focus = current.replace("__AUGMENTPROXY_COMPACT__", "").trim();
    if (focus) lines.push("## Focus", truncateMiddle(focus, 600), "");
  lines.push("## Recent Conversation");

  for (const item of exchanges) {
    if (!item || typeof item !== "object" || Array.isArray(item)) continue;
    const record = item as JsonObject;
    const requestId = text(record.request_id).trim();
    const requestNodes = asArray(record.request_nodes);
    const responseNodes = asArray(record.response_nodes);
    const userText = requestNodes.map(nodeText).filter(Boolean).join("\n") ||
      text(record.request_message).trim();
    const toolResults = requestNodes
      .map((node) => {
        if (!node || typeof node !== "object" || Array.isArray(node)) return "";
        const toolResult = (node as JsonObject).tool_result_node;
        if (!toolResult || typeof toolResult !== "object" || Array.isArray(toolResult)) return "";
        const result = toolResult as JsonObject;
        const id = text(result.tool_use_id).trim();
        const content = text(result.content).trim();
        return content ? `Tool result ${id}: ${truncateMiddle(content, 350)}` : "";
      })
      .filter(Boolean);
    const assistantText = responseNodes.map(nodeText).filter(Boolean).join("\n") ||
      text(record.response_text).trim();
    const toolUses = responseNodes
      .map(nodeToolUse)
      .filter((call): call is JsonObject => Boolean(call))
      .map((call) => {
        const fn = call.function && typeof call.function === "object" && !Array.isArray(call.function)
          ? call.function as JsonObject
          : {};
        return `Tool call ${text(fn.name)}: ${truncateMiddle(text(fn.arguments), 220)}`;
      });
    if (requestId) lines.push(`\n### Exchange ${requestId}`);
    if (userText) lines.push(`User: ${truncateMiddle(userText, 500)}`);
    for (const result of toolResults.slice(-2)) lines.push(result);
    if (assistantText) lines.push(`Assistant: ${truncateMiddle(assistantText, 700)}`);
    for (const use of toolUses.slice(-3)) lines.push(use);
  }

  lines.push(
    "",
    "## Continuation Guidance",
    "Continue from this summary. Do not re-read unrelated history unless needed. Prefer checking current files or running focused verification before editing.",
  );
  return lines.join("\n").trim();
}

function nodeToolUse(node: JsonValue): JsonObject | undefined {
  if (!node || typeof node !== "object" || Array.isArray(node)) {
    return undefined;
  }
  const record = node as JsonObject;
  const toolUse = record.tool_use;
  if (!toolUse || typeof toolUse !== "object" || Array.isArray(toolUse)) {
    return undefined;
  }
  const tool = toolUse as JsonObject;
  const name = typeof tool.tool_name === "string" ? tool.tool_name : "unknown";
  const id = typeof tool.tool_use_id === "string"
    ? tool.tool_use_id
    : `tool_${crypto.randomUUID()}`;
  const args = typeof tool.input_json === "string"
    ? tool.input_json
    : JSON.stringify(tool.input_json ?? {});
  return { id, type: "function", function: { name, arguments: args } };
}

interface HistoryToMessagesResult {
  messages: OpenAIMessage[];
  filteredToolCalls: number;
  filteredToolCallIds: string[];
  filteredToolReasons: string[];
}

function historyToMessages(
  history: JsonValue,
  fallbackPath?: string,
): HistoryToMessagesResult {
  const messages: OpenAIMessage[] = [];
  const filteredToolCallIds = new Set<string>();
  const filteredToolReasons: string[] = [];
  let filteredToolCalls = 0;
  for (const item of asArray(history)) {
    if (!item || typeof item !== "object" || Array.isArray(item)) continue;
    const record = item as JsonObject;

    const requestNodes = asArray(record.request_nodes);
    const responseNodes = asArray(record.response_nodes);
    const toolResults = toolResultSummaries(requestNodes, filteredToolCallIds);
    const responseText =
      responseNodes.map(nodeText).filter(Boolean).join("\n") ||
      text(record.response_text).trim();
    const toolCalls: JsonObject[] = [];
    for (const call of responseNodes.map(nodeToolUse).filter((
      value,
    ): value is JsonObject => Boolean(value))) {
      const parsed = parseToolCall(call, fallbackPath);
      const dropReason = parsed
        ? historicalToolCallDropReason(
          parsed.name,
          parsed.argumentsJson,
          toolCallArgumentsJson(call),
        )
        : undefined;
      if (parsed && dropReason) {
        filteredToolCalls += 1;
        filteredToolCallIds.add(parsed.id);
        filteredToolReasons.push(`${parsed.name}: ${dropReason}`);
        continue;
      }
      toolCalls.push(call);
    }
    const userText = requestNodes.map(nodeText).filter(Boolean).join("\n") ||
      text(record.request_message).trim();

    if (
      toolResults.length > 0 && !userText && !responseText &&
      toolCalls.length === 0
    ) {
      messages.push({
        role: "user",
        content: [
          "Previous tool results were recorded without a following assistant continuation. Treat them as context and continue the task.",
          ...toolResults,
        ].join("\n\n"),
      });
      continue;
    }

    if (
      toolResults.length === 0 && userText && isContinuationText(userText) &&
      !responseText && toolCalls.length === 0
    ) {
      continue;
    }

    // Auggie stores tool results in the next turn's request_nodes. For OpenAI
    // chat-completions, those tool messages must appear immediately after the
    // assistant tool_calls that produced them, before the next assistant turn.
    appendToolResultMessages(messages, requestNodes, filteredToolCallIds);
    if (userText) messages.push({ role: "user", content: userText });

    if (responseText || toolCalls.length > 0) {
      messages.push({
        role: "assistant",
        content: responseText,
        ...(toolCalls.length ? { tool_calls: toolCalls } : {}),
      });
    }

    if (messages.length === 0) {
      const roleRaw = text(record.role || record.speaker || record.type)
        .toLowerCase();
      const role: "user" | "assistant" =
        roleRaw.includes("assistant") || roleRaw.includes("response")
          ? "assistant"
          : "user";
      const content = text(record.content || record.text || record.message)
        .trim();
      if (content) messages.push({ role, content });
    }
  }
  return {
    messages,
    filteredToolCalls,
    filteredToolCallIds: [...filteredToolCallIds],
    filteredToolReasons,
  };
}

function historicalToolCallDropReason(
  toolName: string,
  argumentsJson: string,
  originalArgumentsJson: string,
): string | undefined {
  if (toolName !== "str-replace-editor") return undefined;
  const reason = invalidToolReason(toolName, argumentsJson);
  if (!reason) return undefined;
  if (
    reason.includes("requires path") ||
    reason.includes("outside the allowed scope") ||
    reason.includes("path does not exist") ||
    reason.includes("no insert_line_entries")
  ) return reason;
  if (reason.includes("no effective str_replace_entries")) {
    const rawArgs = parseHistoricalStrReplaceArgs(originalArgumentsJson);
    const rawEntries = Array.isArray(rawArgs?.str_replace_entries)
      ? rawArgs.str_replace_entries
      : [];
    if (rawEntries.length === 0 || rawEntries.every(isNoopStrReplaceEntry)) {
      return reason;
    }
  }
  return undefined;
}

function toolCallArgumentsJson(call: JsonObject): string {
  const fn = call.function;
  if (!fn || typeof fn !== "object" || Array.isArray(fn)) return "{}";
  const args = (fn as JsonObject).arguments;
  return typeof args === "string" ? args : JSON.stringify(args ?? {});
}

function parseHistoricalStrReplaceArgs(
  argumentsJson: string,
): JsonObject | undefined {
  const parsed = parseStrReplaceObjectFromPossiblyBrokenJson(argumentsJson);
  if (parsed) return parsed;
  const outer = parseJsonObjectLoose(argumentsJson);
  if (outer && typeof outer.raw_input === "string") {
    return parseStrReplaceObjectFromPossiblyBrokenJson(outer.raw_input) ??
      parseJsonObjectLoose(outer.raw_input);
  }
  return outer;
}

function isNoopStrReplaceEntry(entry: JsonValue): boolean {
  if (!entry || typeof entry !== "object" || Array.isArray(entry)) return false;
  const record = entry as JsonObject;
  return typeof record.old_str === "string" &&
    typeof record.new_str === "string" &&
    normalizeForNoopCompare(record.old_str) ===
      normalizeForNoopCompare(record.new_str);
}

function toolResultSummaries(
  nodes: JsonValue,
  skipToolCallIds = new Set<string>(),
): string[] {
  const output: string[] = [];
  for (const node of asArray(nodes)) {
    if (!node || typeof node !== "object" || Array.isArray(node)) continue;
    const toolResult = (node as JsonObject).tool_result_node;
    if (
      !toolResult || typeof toolResult !== "object" ||
      Array.isArray(toolResult)
    ) continue;
    const result = toolResult as JsonObject;
    const id = text(result.tool_use_id).trim();
    if (id && skipToolCallIds.has(id)) continue;
    const content = compactToolResultContent(text(result.content));
    if (!content.trim()) continue;
    output.push(`Tool result${id ? ` ${id}` : ""}:\n${content}`);
  }
  return output;
}

function appendToolResultMessages(
  messages: OpenAIMessage[],
  nodes: JsonValue,
  skipToolCallIds = new Set<string>(),
): void {
  for (const node of asArray(nodes)) {
    if (!node || typeof node !== "object" || Array.isArray(node)) continue;
    const toolResult = (node as JsonObject).tool_result_node;
    if (
      !toolResult || typeof toolResult !== "object" || Array.isArray(toolResult)
    ) continue;
    const result = toolResult as JsonObject;
    const toolCallId = typeof result.tool_use_id === "string"
      ? result.tool_use_id
      : undefined;
    if (toolCallId && skipToolCallIds.has(toolCallId)) continue;
    const content = compactToolResultContent(text(result.content));
    if (toolCallId) {
      messages.push({ role: "tool", tool_call_id: toolCallId, content });
    }
  }
}

function currentNodeUserText(nodes: JsonValue): string {
  return asArray(nodes).map(nodeText).filter(Boolean).join("\n").trim();
}

function hasToolResultNodes(nodes: JsonValue): boolean {
  return asArray(nodes).some((node) => {
    if (!node || typeof node !== "object" || Array.isArray(node)) return false;
    return Boolean((node as JsonObject).tool_result_node);
  });
}

function isContinuationText(value: string): boolean {
  const normalized = value.trim().toLowerCase();
  return normalized === "继续" || normalized === "continue" ||
    normalized === "go on" || normalized === "next" ||
    normalized.includes("继续");
}

function shouldRetryStalledContinuation(ctx: RequestContext): boolean {
  const body = objectBody(ctx);
  const currentText = currentNodeUserText(body.nodes) || text(body.message) ||
    text(body.prompt) || text(body.instruction);
  return hasToolResultNodes(body.nodes) || isContinuationText(currentText);
}

function pruneUnresolvedToolHistory(messages: OpenAIMessage[]): {
  messages: OpenAIMessage[];
  prunedAssistantTurns: number;
  prunedToolMessages: number;
  prunedToolCallIds: string[];
} {
  const output: OpenAIMessage[] = [];
  let prunedAssistantTurns = 0;
  let prunedToolMessages = 0;
  const prunedToolCallIds: string[] = [];

  for (let index = 0; index < messages.length;) {
    const message = messages[index];

    if (message.role === "tool") {
      prunedToolMessages += 1;
      if (message.tool_call_id) prunedToolCallIds.push(message.tool_call_id);
      index += 1;
      continue;
    }

    if (
      message.role !== "assistant" || !Array.isArray(message.tool_calls) ||
      message.tool_calls.length === 0
    ) {
      output.push(message);
      index += 1;
      continue;
    }

    const expectedIds = message.tool_calls
      .map((call) =>
        call && typeof call === "object" && !Array.isArray(call) &&
          typeof (call as JsonObject).id === "string"
          ? (call as JsonObject).id as string
          : undefined
      )
      .filter((value): value is string => Boolean(value));

    if (expectedIds.length === 0) {
      output.push({
        role: "assistant",
        content: message.content,
      });
      index += 1;
      continue;
    }

    const expected = new Set(expectedIds);
    const seen = new Set<string>();
    const gathered: OpenAIMessage[] = [];
    let malformed = false;
    let cursor = index + 1;

    while (cursor < messages.length) {
      const next = messages[cursor];
      if (next.role !== "tool") break;
      gathered.push(next);
      if (
        typeof next.tool_call_id !== "string" || !expected.has(next.tool_call_id) ||
        seen.has(next.tool_call_id)
      ) {
        malformed = true;
      } else {
        seen.add(next.tool_call_id);
      }
      cursor += 1;
    }

    const complete = !malformed && seen.size === expected.size;
    if (complete) {
      output.push(message);
      output.push(...gathered);
      index = cursor;
      continue;
    }

    prunedAssistantTurns += 1;
    prunedToolMessages += gathered.length;
    prunedToolCallIds.push(...expectedIds);
    for (const toolMessage of gathered) {
      if (toolMessage.tool_call_id) prunedToolCallIds.push(toolMessage.tool_call_id);
    }
    if (message.content.trim()) {
      output.push({
        role: "assistant",
        content: message.content,
      });
    }
    index = cursor;
  }

  return {
    messages: output,
    prunedAssistantTurns,
    prunedToolMessages,
    prunedToolCallIds,
  };
}

function buildMessages(
  config: ProxyConfig,
  ctx: RequestContext,
): OpenAIMessage[] {
  const body = objectBody(ctx);
  const systemParts: string[] = [];
  addIfPresent(systemParts, "System prompt", body.system_prompt);
  addIfPresent(systemParts, "System prompt append", body.system_prompt_append);
  addIfPresent(systemParts, "User guidelines", body.user_guidelines);
  addIfPresent(systemParts, "Workspace guidelines", body.workspace_guidelines);
  addIfPresent(systemParts, "Rules", body.rules);
  addIfPresent(systemParts, "Skills", body.skills);
  systemParts.push(toolUseSystemPrompt(ctx));

  const messages: OpenAIMessage[] = [];
  if (systemParts.length > 0) {
    messages.push({ role: "system", content: systemParts.join("\n\n") });
  }
  const fallbackPath = workspaceFallbackPath(ctx);
  const historyMessages = historyToMessages(body.chat_history, fallbackPath);
  messages.push(...historyMessages.messages);
  if (historyMessages.filteredToolCalls > 0) {
    logWarn(config, "openai:messages:filtered-invalid-tool-history", {
      requestId: ctx.requestId,
      filteredToolCalls: historyMessages.filteredToolCalls,
      sampleToolCallIds: historyMessages.filteredToolCallIds.slice(0, 12),
      sampleReasons: historyMessages.filteredToolReasons.slice(0, 5),
    });
    messages.push({
      role: "user",
      content:
        "Proxy context: previous invalid str-replace-editor calls were removed from tool history. Do not repeat those old edit arguments. Re-read the current file and generate fresh old_str/new_str pairs before editing.",
    });
  }
  appendToolResultMessages(messages, body.nodes);
  const currentUserText = currentNodeUserText(body.nodes);
  const currentHasToolResults = hasToolResultNodes(body.nodes);

  const userParts: string[] = [];
  addIfPresent(
    userParts,
    "Message",
    currentUserText || body.message || body.prompt || body.instruction,
  );
  addIfPresent(userParts, "Path", body.path);
  addIfPresent(userParts, "Language", body.lang || body.language);
  addIfPresent(
    userParts,
    "Selected code",
    body.selected_code || body.selected_text,
  );
  addIfPresent(userParts, "Prefix", body.prefix);
  addIfPresent(userParts, "Suffix", body.suffix);
  addIfPresent(userParts, "Blobs", body.blobs);
  addIfPresent(userParts, "User guided blobs", body.user_guided_blobs);

  if (userParts.length > 0) {
    messages.push({ role: "user", content: userParts.join("\n\n") });
  } else if (currentHasToolResults) {
    messages.push({
      role: "user",
      content:
        "Continue from the latest tool result. If the task is not complete, perform the next concrete step using an appropriate tool. If it is complete, summarize the result and next step briefly.",
    });
  } else {
    messages.push({ role: "user", content: "Continue." });
  }
  const repaired = pruneUnresolvedToolHistory(messages);
  if (repaired.prunedAssistantTurns > 0 || repaired.prunedToolMessages > 0) {
    logWarn(config, "openai:messages:pruned-unresolved-tool-history", {
      requestId: ctx.requestId,
      before: messages.length,
      after: repaired.messages.length,
      prunedAssistantTurns: repaired.prunedAssistantTurns,
      prunedToolMessages: repaired.prunedToolMessages,
      sampleToolCallIds: repaired.prunedToolCallIds.slice(0, 12),
    });
  }
  return repaired.messages;
}

function hasPriorTurns(ctx: RequestContext): boolean {
  const body = objectBody(ctx);
  if (asArray(body.chat_history).length > 0) return true;
  for (const node of asArray(body.nodes)) {
    if (!node || typeof node !== "object" || Array.isArray(node)) continue;
    if ((node as JsonObject).tool_result_node) return true;
  }
  return false;
}

function hasDirectoryListingResult(ctx: RequestContext, path: string): boolean {
  const body = objectBody(ctx);
  const normalized = cleanExtractedPath(path);
  const isListingText = (content: string): boolean => {
    if (!content) return false;
    const lower = content.toLowerCase();
    if (!lower.includes("files and directories")) return false;
    return content.includes(normalized);
  };

  for (const node of asArray(body.nodes)) {
    if (!node || typeof node !== "object" || Array.isArray(node)) continue;
    const result = (node as JsonObject).tool_result_node;
    if (!result || typeof result !== "object" || Array.isArray(result)) {
      continue;
    }
    const content = text((result as JsonObject).content);
    if (isListingText(content)) return true;
  }

  for (const item of asArray(body.chat_history).slice(-10)) {
    if (!item || typeof item !== "object" || Array.isArray(item)) continue;
    const record = item as JsonObject;
    for (const node of asArray(record.request_nodes)) {
      if (!node || typeof node !== "object" || Array.isArray(node)) continue;
      const result = (node as JsonObject).tool_result_node;
      if (!result || typeof result !== "object" || Array.isArray(result)) {
        continue;
      }
      const content = text((result as JsonObject).content);
      if (isListingText(content)) return true;
    }
  }

  return false;
}

function toolUseSystemPrompt(ctx: RequestContext): string {
  const body = objectBody(ctx);
  const workspacePath = typeof body.path === "string" && body.path
    ? body.path
    : undefined;
  const toolDefinitions = Array.isArray(body.tool_definitions)
    ? body.tool_definitions
    : [];
  const toolSummaries = toolDefinitions
    .filter((item): item is JsonObject =>
      !!item && typeof item === "object" && !Array.isArray(item) &&
      typeof (item as JsonObject).name === "string"
    )
    .map((tool) => toolPromptSummary(tool))
    .filter(Boolean);

  const lines = [
    "Auggie/Codex-style tool protocol:",
    "- You are an autonomous coding agent. Keep working until the user's request is fully resolved; do not stop after only announcing a plan or after one tool call.",
    "- Before using tools, briefly state what you are about to inspect or do. After tool results, continue from those exact results instead of repeating the same call.",
    "- Use tools only through the provided function-calling interface. Do not write XML, Markdown tool blocks, or prose pretending to be a tool call.",
    "- Every function call argument must be one complete JSON object that satisfies the tool schema. Never call a tool with {} unless that tool schema explicitly has no required fields.",
    "- Do not invent paths. Use paths from the current request, conversation, workspace context, search results, or directory listings.",
    "- Path safety policy: never access /, /home, or any path outside /home/<current-user>/. Restrict file and directory operations to the current user's home workspace only.",
    "- If a required argument is unknown, first use a discovery tool with a known directory/path or answer from available context; do not emit an invalid call.",
    "- If a tool fails validation, repair the next tool call by providing the missing required JSON field; do not repeat the same invalid call.",
    "- For view-range-untruncated and search-untruncated, reference_id must come from the truncation footer text 'Reference ID: ...'. Never use a tool_use_id like call_function_* as reference_id.",
    "- If str-replace-editor reports old_str not found or no changes, do not repeat the same edit call. Re-read the file and regenerate fresh old_str/new_str from current content.",
    "- For launch-process checks that may legitimately return no matches (for example grep probes), append `|| true` to avoid unnecessary hard-failure retries.",
    "- For project evaluation, inspect the workspace root/directory first, then read specific files discovered from listings, then synthesize a final answer.",
    '- Final-answer format is mandatory: after the main answer, append a section titled "Next Steps" with 1-3 numbered, concrete, executable follow-up actions tailored to the user\'s goal. Do not omit this section.',
    "- If you already have a directory listing result, do not call view on the same root directory again in later turns. Move forward by reading specific files or using codebase-retrieval with a concrete information_request.",
  ];
  if (workspacePath) {
    lines.push(
      `- Current workspace/path from the client: ${workspacePath}. Use it as the starting directory when you need to inspect this project.`,
    );
    if (!hasPriorTurns(ctx) && !hasDirectoryListingResult(ctx, workspacePath)) {
      lines.push(
        `- For the initial directory inspection only, call view with {"path":"${workspacePath}","type":"directory"}. Do not repeat that same directory call in later turns unless new information requires it.`,
      );
    } else if (hasDirectoryListingResult(ctx, workspacePath)) {
      lines.push(
        `- Directory listing for ${workspacePath} is already available in prior tool results. Do NOT call view on ${workspacePath} again; continue with concrete files/subdirectories from that listing.`,
      );
    }
  }
  lines.push(
    "Critical tool argument formats:",
    '- view: requires a concrete path. Valid examples: {"path":"<known-file>","type":"file"} or {"path":"<known-directory>","type":"directory"}. Invalid: {}, {"type":"file"}, {"path":""}.',
    "- view-range-untruncated: requires reference_id, start_line, end_line from a prior view result. Do not pass path to this tool.",
    '- codebase-retrieval: requires information_request. Valid example: {"information_request":"Find the modules responsible for request routing, OpenAI adaptation, indexing, and configuration."}. Invalid: {}.',
    '- launch-process: requires command. Prefer simple commands and set cwd only when known. Valid example: {"command":"pwd && ls -la","cwd":"<known-directory>"}.',
    '- save-file: requires path and file_content. Valid example: {"path":"<file>","file_content":"complete file contents"}.',
    "- str-replace-editor: only use when editing is explicitly needed, and provide the complete required schema fields.",
  );
  if (toolSummaries.length > 0) {
    lines.push("Available tool schemas from this client:");
    lines.push(...toolSummaries.slice(0, 30));
  }
  return lines.join("\n");
}

function toolPromptSummary(tool: JsonObject): string {
  const name = typeof tool.name === "string"
    ? normalizeToolName(tool.name)
    : "unknown";
  const schema = parseToolSchema(tool);
  const required = Array.isArray(schema.required)
    ? schema.required.filter((item) => typeof item === "string")
    : [];
  const properties =
    schema.properties && typeof schema.properties === "object" &&
      !Array.isArray(schema.properties)
      ? Object.keys(schema.properties as JsonObject)
      : [];
  const requiredText = required.length > 0 ? required.join(", ") : "none";
  const propertyText = properties.length > 0
    ? properties.slice(0, 12).join(", ")
    : "unspecified";
  return `- ${name}: required=[${requiredText}], fields=[${propertyText}]`;
}

function parseToolSchema(tool: JsonObject): JsonObject {
  const schema = tool.input_schema_json;
  if (typeof schema === "string" && schema.trim()) {
    try {
      const parsed = JSON.parse(schema) as JsonObject;
      if (parsed && typeof parsed === "object" && !Array.isArray(parsed)) {
        return parsed;
      }
    } catch {
      return { type: "object", properties: {} };
    }
  }
  const inputSchema = tool.input_schema;
  if (
    inputSchema && typeof inputSchema === "object" &&
    !Array.isArray(inputSchema)
  ) return inputSchema as JsonObject;
  const parameters = tool.parameters;
  if (
    parameters && typeof parameters === "object" && !Array.isArray(parameters)
  ) return parameters as JsonObject;
  return { type: "object", properties: {} };
}

